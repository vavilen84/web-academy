/*
Объявим функцию при помощи слова function. Далее следует имя функции, при помощи которого мы и дальше сможем вызывать ее.
Внутри круглых скобок мы можем передавать аргументы. Внутри фигурных скобок само тело функции.
*/
function getLanguageTitle () {
    /*
    Объявим переменную и запишем туда название нашего языка
    */
    $title = "PHP";
    /*
    return прекращает работу функции и возвращает значение(в данном случае значение переменной $title)
    */
    return $title;
}
/*
Объявим переменную $title и поместим туда результат работы функции - строку "PHP"
*/
$title = getLanguageTitle();
/*
Выведем содержимое переменной на экран
*/
echo $title;
