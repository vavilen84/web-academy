    Итак, напишем нашу функцию. Функция ничего не должна выводить на экран посредством функцкции есно - она должна
    возвращать резльтат своей работы посредством функции return. После вызова функции return - пользовательская функция
    (тоесть написанная нами) прекратит свою работу и вернет результат переданный функции return. Выводим результат работы отдельно.
    Еще раз напомню, комментарии (то что после //) писать не надо - это пояснения.
