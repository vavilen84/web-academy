    Функции - это основополагающая часть PHP сценария. Если проще, то это часть кода, которая предназначена выполнить свою
    небольшую задачу. Предположим, что у нас сайт с обзором языков програмирования. И нам надо вывести информацию о PHP.
    Напишем небольшую функцию которая вернет нам название языка. Поместим это название в переменную и выведем на экран. Называть
     функции мы должны так, чтобы ее название как бе само говорило что собственно функция делает. Сразу отмечу, что стоит привыкать
     к английскому. Код мы должны писать так, чтобы он был понятен разработчикам, говорящим на разных языках. Английский
    тут что то вроде международного. Так что если название функций неясно, то разбиваем по словам ('get Language Title') и
    гуглим перевод. Функции (как и переменные) называем без сокращенных слов, но как можно короче. Именуем функции
    в верблюжьем стиле (camelCase).
